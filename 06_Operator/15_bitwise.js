// bitwise and

let a = 2;
let b = 3;
console.log(a & b);

// bitwise and assignment

a = 2;
b = 3;
a &= b;
console.log(a);

// bitwise or
a = 2;
b = 3;
console.log(a | b);

// bitwise or assignment
a = 2;
b = 3;
a |= b;
console.log(a);

// bitwise NOT
a = 2;
console.log(~a);

// bitwise xor
a = 2;
b = 3;
console.log(a ^ b);

// bitwise xor assignment
a = 2;
b = 3;
a ^= b;
console.log(a);

// left shift
a = 5;
console.log(a << 2);

// left shift assignment
a = 5;
b = 2;
a <<= b;
console.log(a);

// right shift
a = 5;
console.log(a >> 2);

// right shift assignment
a = 5;
b = 2;
a >>= b;
console.log(a);
