// Shift()
let students = ["Kumar", "Aureen", "Joes", "Zara", "Stanley", "Rajesh"];

console.log("Before shift : ", students);
let element = students.shift();
console.log("After shift : ", students);
console.log("Removed Element : ", element);

console.log("Before shift : ", students);
element = students.shift();
console.log("After shift : ", students);
console.log("Removed Element : ", element);
